<script>
import { ref } from "vue";
import { gsap } from "gsap";
import { TextPlugin } from "gsap/TextPlugin";
import ScrollTrigger from "gsap/ScrollTrigger";
gsap.registerPlugin(ScrollTrigger);
gsap.registerPlugin(TextPlugin);

import { Marquee } from "@selemondev/vue3-marquee";
// import "@selemondev/vue3-marquee/dist/style.css";

export default {
  name: "values",
  props: {
    pageId: {
      type: Number,
    },
    section: {
      type: Object,
    },
  },
  components: {
    Marquee,
  },
  data() {
    return {
      values: this.section,
    };
  },
  mounted() {
    this.$nextTick(() => {
      setTimeout(() => {
        ScrollTrigger.refresh(); 
        this.pinElements();
        this.textAnimTransformadoras();
        this.textAnimEntusiastas();
        this.textAnimConsolidadas();
        this.textAnimIntuitivas();
        this.textAnimAccesibles();
      }, 500);
    });
  },
  beforeUnmount() {
    // Se destruyen todos los triggers para evitar conflictos al navegar
    ScrollTrigger.getAll().forEach((trigger) => trigger.kill());
    ScrollTrigger.refresh();
  },
  methods: {
    pinElements() {
      if (this.$refs.sectionValues) {
        const footer = document.querySelector("footer");
        gsap.utils.toArray(".section-values li").forEach((container, i) => {
          let prev = container.previousElementSibling;

          ScrollTrigger.create({
            trigger: container,
            start: "bottom bottom",
            endTrigger: footer,
            end: "top bottom",
            pin: true,
            // markers: true,
            pinSpacing: false,
          });

      

          if (prev) {
            gsap.to(prev, {
              opacity: 0,
              scrollTrigger: {
                trigger: container,

                start: "top 90%",
                scrub: 1,
                toggleActions: "play none reverse reset",
              },
            });
          }
        });
      }
    },
    textAnimTransformadoras() {
      const transformadorasTL = gsap.timeline({ repeat: -1, delay: 2 });
      const transformadorasText = document.querySelector(".transformadoras p");
      transformadorasTL
        .to(transformadorasText, {
          duration: 1,
          delay: 1,
          delimiter: " ",
          ease: "sine.inOut",
          text: {
            value: "sarodamrofsnart",
          },
        })
        .to(transformadorasText, {
          duration: 1,
          delay: 0,
          delimiter: " ",
          ease: "sine.inOut",
          text: {
            value: "transformadoras",
          },
        });
    },
    textAnimEntusiastas() {
      const entusiastasTL = gsap.timeline({ repeat: -1, yoyo: true  });
      const entusiastasText = document.querySelector(".entusiastas svg");
      const dot = entusiastasText.querySelector("#punto");
      entusiastasTL
        .to(dot, {
          delay: 0,
          yPercent: -100,
          duration: 0.5,
          transformOrigin: "0% 0%",
        })
        
    },
    textAnimConsolidadas() {
  const consolidadasText = document.querySelector(".consolidadas p");

  const consolidadasTL = gsap.timeline({
    repeat: -1,
    delay: 2,
    repeatDelay: 4,
    yoyo: false,
  });

  // Aseguramos perspectiva en el contenedor
  gsap.set(".consolidadas", { perspective: 800 });

  // Estilos iniciales del texto
  gsap.set(consolidadasText, {
    transformStyle: "preserve-3d",
    backfaceVisibility: "hidden",
  });

  consolidadasTL
    // SALE volteando hacia atrás
    .to(consolidadasText, {
      rotateX: 90,
      duration: 0.4,
      ease: "power2.in",
      onComplete: () => {
        consolidadasText.textContent = "consolidadas";
      },
    })

    // ENTRA desde atrás hacia adelante
    .to(consolidadasText, {
      rotateX: 0,
      duration: 0.5,
      ease: "back.out(1.4)",
    })

    // SEGUNDO CAMBIO DE TEXTO
    .to(consolidadasText, {
      rotateX: 90,
      duration: 0.4,
      ease: "power2.in",
      delay: 2,
      onComplete: () => {
        consolidadasText.textContent = "CONSOLIDADAS";
      },
    })

    // VUELVE A APARECER
    .to(consolidadasText, {
      rotateX: 0,
      duration: 0.5,
      ease: "back.out(1.4)",
    });
},
    textAnimIntuitivas() {
      const intuitivasTL = gsap.timeline({ repeat: -1, delay: 0, repeatDelay: 3, yoyo:true });
      const intuitivasText = document.querySelector(".intuitivas");
      const intuitivasVocales = intuitivasText.querySelectorAll(".vocal");
      const intuitivasNoVocales = intuitivasText.querySelectorAll(":not(.vocal)");

      gsap.set(intuitivasVocales, {
        yPercent: 110,
      })

      gsap.set(intuitivasNoVocales, {
        autoAlpha: 0,
      })

      intuitivasTL
      .to(intuitivasNoVocales, {
         autoAlpha: 1,
          stagger: .08,
        })
        .to(intuitivasVocales, {
          yPercent: 0,
          stagger: .08,
          ease: "power2.inOut",
        },"-=.6")
        
    },
    textAnimAccesibles() {
      const accesiblesTL = gsap.timeline({ repeat: -1, yoyo: true });
      const accesiblesText = document.querySelector(".accesibles");
      const c = accesiblesText.querySelector("#c");
      const move = accesiblesText.querySelectorAll(".move");
      const movePre = accesiblesText.querySelectorAll(".movepre");
      gsap.set(c, {
        rotate: -90,
        transformOrigin: "50% 50%",
      })

      gsap.set(move, {
         x: "10px",
          transformOrigin: "50% 50%",
      })

       gsap.set(movePre, {
         x: "-10px",
          transformOrigin: "50% 50%",
      })

      accesiblesTL
        .to(c, {
          rotate: 0,
          transformOrigin: "50% 50%",
          delay: 1,
          duration: 0.5,
          ease: "back.out(1.7)",
        }).to(move, {
          x: "0px",
          transformOrigin: "50% 50%",
          ease: "back.out(1.7)",
        }, "<").to(movePre, {
          x: "0px",
          transformOrigin: "50% 50%",
          ease: "back.out(1.7)",
        }, "<")
    },
  },
};
</script>

